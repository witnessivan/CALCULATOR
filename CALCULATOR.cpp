double addx(double a, double b) {
	return a + b;
}
double addy(double c, double d) {
	return c + d;
}
double substractx(double a, double b) {
	return a - b;
}
double substracty(double c, double d) {
	return c - d;
}
double multiplyx(double a,  double n) {
	return a * n;
}
double multiplyy(double c, double n) {
	return c * n;
}
double stray(double a, double b, double c, double d) {
	return a * b + c * d;
}
